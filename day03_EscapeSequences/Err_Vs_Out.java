package day03_EscapeSequences;

public class Err_Vs_Out {

    public static void main(String[] args) {

        System.out.println("Hello Cydeo");
        System.out.println("How are you all day");

        System.out.println(",,,,,,,,,");

        System.out.print("Hello Cydeo\n"
        + "How are you all day\n" + "What is up?");
        System.out.println("-----------------------");

        System.out.println("Hello my friend \nHow are today? \nAre you okey?");
        System.out.println("My favorite tv show is \"Game Of Thrones\"");

        System.out.println("\t\t\tJava is cool programming");
        String bookName="The rich dad and the rich mum";
        System.out.println("My favorite book name is" + "\"" +bookName+"\"");
    }

}
