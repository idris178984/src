package day09_IfStatement;

public class NameOfDays {
    public static void main(String[] args) {

        int n = 5; // 1~7

        if (n == 1) {
            System.out.println("Monday" );

        } else if (n == 2) {
            System.out.println("tuesday" );
        } else if (n == 3) {
            System.out.println("Wednesday" );
        } else if (n == 5) {
            System.out.println("Friday" );
        }


        System.out.println("--------------" );


    }
}
